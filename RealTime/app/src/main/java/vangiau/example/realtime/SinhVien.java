package vangiau.example.realtime;

public class SinhVien {
    public String giatri;
    public String hinhanhthietbi;
    public String id;
    public String idthietbi;
    public String mota;
    public String tenthietbi;

    public SinhVien() {
    }

    public SinhVien(String giatri, String hinhanhthietbi, String id, String idthietbi, String mota, String tenthietbi) {
        this.giatri = giatri;
        this.hinhanhthietbi = hinhanhthietbi;
        this.id = id;
        this.idthietbi = idthietbi;
        this.mota = mota;
        this.tenthietbi = tenthietbi;
    }
}
